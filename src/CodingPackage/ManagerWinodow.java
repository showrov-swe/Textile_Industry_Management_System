    private void BtnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnSaveMouseClicked
        String QUERY = "INSERT into Deals (CompanyName,ProId,ProName,Quantity,Bill,DeliveryDate,DelivStatus,ProfitPerOrder,MId)"
                +" values(?,?,?,?,?,?,?,?,?)";
        try 
        {
            PreparedStatement pt = connected.prepareStatement(QUERY);
            
            pt.setString(1, TxfCompanyName.getText());
            pt.setString(2, SelectedProductId);
            pt.setString(3, ComboProduct.getSelectedItem().toString());
            pt.setString(4, TxfQuantity.getText());
            pt.setString(5, TxfBill.getText());
            pt.setString(6, deliveryDate.getText());
            
            if(PendingStatus.isSelected())
            {
                pt.setString(7, "Pending");
            }
            if(OnProgressStatus.isSelected())
            {
                pt.setString(7, "On Progress");
            }
            if(DeliveredStatus.isSelected())
            {
                pt.setString(7, "Delivered");
            }
            
            
            Double PerProfit=0.0;
            try 
            {
                Statement st = connected.createStatement();
                ResultSet rst = st.executeQuery("SELECT *FROM Products WHERE ProName ='"+ComboProduct.getSelectedItem().toString()+"'");
                if(rst.next())
                {
                    PerProfit = rst.getDouble("PerProfit");
                }
                st.close();
                rst.close();
            } 
            catch (SQLException e)
            {
               JOptionPane.showMessageDialog(null, e.toString());
            }
            
            pt.setString(8, String.valueOf(Integer.parseInt(TxfQuantity.getText())*PerProfit));
            pt.setString(9, LoggedAs);
            pt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
            
            clearAllDealForm();
            
            DefaultTableModel model = (DefaultTableModel) OrderTable.getModel();
            model.setRowCount(0);
            showOrders();
            
        } 
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_BtnSaveMouseClicked

