    private void TxfQuantityKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxfQuantityKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            if (isNumeric(TxfQuantity.getText())==false)
            {
                JOptionPane.showMessageDialog(null, "Enter Numeric Value");
                TxfQuantity.setText("");
                TxfBill.setText("");
            }
            
            else
            {
            double priceGetter=0.0;
            try 
            {
                Statement billStatement = connected.createStatement();
                ResultSet rst = 
                billStatement.executeQuery("SELECT *FROM Products WHERE ProName = '"+ComboProduct.getSelectedItem().toString()+"'");
                
                while(rst.next())
                {
                    priceGetter = rst.getDouble("SellPrice");
                }
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null, e.toString());
            }
            
            TxfBill.setText(String.valueOf((double)priceGetter*Integer.parseInt(TxfQuantity.getText())));
            requiredDays.requestFocus();
            }
        }
    }//GEN-LAST:event_TxfQuantityKeyPressed

