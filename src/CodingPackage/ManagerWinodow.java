    private static void showOrders()
    {
        DefaultTableModel model = (DefaultTableModel) OrderTable.getModel();
        try 
        {
            Statement stmt = null;
            stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM Deals WHERE MId = '"+
                    LoggedAs+"' AND (DelivStatus = 'Pending' OR DelivStatus ='On Progress');");
            
            while (rs.next())
            {
                Object[] obj = {rs.getInt("DealNumber"),rs.getString("CompanyName"),rs.getString("ProName"),rs.getInt("Quantity"),rs.getDouble("Bill"),rs.getString("DeliveryDate"),rs.getString("DelivStatus")};
                model.addRow(obj);
            }
            rs.close();
            stmt.close();
        } 
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
            
        }
        OrderTable.setBackground(new Color(49,57,66));
        OrderScrollPane.getViewport().setBackground(new Color(49,57,66));
        OrderTable.getTableHeader().setOpaque(false);
        OrderTable.getTableHeader().setForeground(Color.darkGray);
        OrderTable.getTableHeader().setFont(new Font("Century Gothic", Font.PLAIN, 16));
    }
    
